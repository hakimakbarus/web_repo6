    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        //
        $data = \App\User::findOrFail($id);
        $data_role = \App\Role::all();
        return view('dashboardAdmin.user.edit', compact('data'), compact('data_role'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $data = \App\User::findOrFail($id);
        $data->id_role = $request->id_role;
        $data->save();

        return view('dashboardAdmin.user.index', compact('data'));
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
        $data = \App\User::findOrFail($id);
        $data->delete();
        return redirect('dashboardAdmin/user')->with('success', 'Data Berhasil dihapus');
    }
}
