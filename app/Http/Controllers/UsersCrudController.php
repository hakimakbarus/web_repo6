     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
        $data = \App\User::latest()->where('id','<>',auth()->user()->id)->paginate(5);
        return view('dashboardAdmin.user.index', compact('data'))
                ->with('i', (request()->input('page', 1) - 1) * 5);
    }

